CREATE OR REPLACE FUNCTION fn_update_standards_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.strnd_date_update = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER tr_update_standards_timestamp
    BEFORE UPDATE ON ge_standards
    FOR EACH ROW
    EXECUTE FUNCTION fn_update_standards_timestamp();