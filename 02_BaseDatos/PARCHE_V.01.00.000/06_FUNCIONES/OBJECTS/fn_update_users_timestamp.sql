CREATE OR REPLACE FUNCTION fn_update_users_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.usr_date_update = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER tr_update_users_timestamp
    BEFORE UPDATE ON au_users
    FOR EACH ROW
    EXECUTE FUNCTION fn_update_users_timestamp();